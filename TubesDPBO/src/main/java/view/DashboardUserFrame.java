    private void loadDataUser() {
        DefaultTableModel model = (DefaultTableModel) tableDashboardUser.getModel();

        model.setRowCount(0);
        String sql = "SELECT nama, role FROM user";

        try (
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/klinik", "root", ""); Statement st = conn.createStatement(); ResultSet rs = st.executeQuery(sql)) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama"),
                    rs.getString("role")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void loadDataHewan() {
        DefaultTableModel model = (DefaultTableModel) tableDashboardHewan.getModel();
        model.setRowCount(0);

        String sql
                = "SELECT h.nama_hewan, u.nama AS customer "
                + "FROM hewan h JOIN user u ON h.id_customer = u.id_user";

        try (Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/klinik",
                "root", ""); PreparedStatement ps = conn.prepareStatement(sql); ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama_hewan"),
                    rs.getString("customer")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
