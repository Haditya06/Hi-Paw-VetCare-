    private void loadCustomer() {
        cbNamaCustomer.removeAllItems();

        String sql = "SELECT id_user, nama FROM user WHERE role='customer'";

        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                cbNamaCustomer.addItem(
                        rs.getInt("id_user") + " - " + rs.getString("nama")
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    private void loadHewanByCustomer() {

        cbHewan.removeAllItems();

        if (cbNamaCustomer.getSelectedItem() == null) return;

        String selected = cbNamaCustomer.getSelectedItem().toString();
        int idUser = Integer.parseInt(selected.split(" - ")[0]);

        String sql = "SELECT id_hewan, nama_hewan FROM hewan WHERE id_user=?";

        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, idUser);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                cbHewan.addItem(
                        rs.getInt("id_hewan") + " - " + rs.getString("nama_hewan")
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    private void loadLayanan() {

        cbTipeLayanan.removeAllItems();

        String sql = "SELECT id_layanan, nama_layanan FROM layanan";

        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                cbTipeLayanan.addItem(
                        rs.getInt("id_layanan") + " - " + rs.getString("nama_layanan")
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

